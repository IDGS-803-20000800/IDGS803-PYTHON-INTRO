#La función "range" hace una sucesión númerica desde el 0 hasta un número antes del indicado
numeros = range(5) #[0, 1, 2, 3, 4]

#Si se establece un rango, entonces se hará una sucesión númerica desde 
# el inmediato superior del primer número hasta el inmediato inferior del segundo número
numeros = range(2, 8) #[3, 4, 5, 6, 7]

#Un tercer argumento representa el aumento en el que irá la sucesión
numeros = range(0, 10, 2) #[2, 4, 6, 8]